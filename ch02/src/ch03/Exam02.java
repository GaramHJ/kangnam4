package ch03;

public class Exam02 {

	public static void main(String[] args) {
		
		/* 원주율 pi는 3.1415로 정의하고
		 * 원의 반지름이 2.5인 원의 넓이를 구하는 공식은
		 * area = pi * 5^2 이다. 이 식을 이용하여
		 * 원의 넓이 area를 실수로 출력하시오.
		 * 단, pi는 상수이다.
		 */
		
		double PI = 3.1415;
		double area = PI * 2.5 * 2.5;
		System.out.println(area);
		
		

	}

}
